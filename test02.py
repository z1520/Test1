# 2.测试相关知识分类
#
# 黑盒测试：功能测试，功能符合用户要求
# 白盒测试：知道里面的逻辑，验证符合用户的需求
# 冒烟测试：主功能、主路径测试，测试主要流程
# 自动化测试：人工测试转为工具自动执行
# 性能测试：模拟各种峰值负载去测试各项性能指标  Jmeter  Loadrunner
#
# 3.自动化测试和手工测试的优缺点
#
# 自动化测试方便，自动验证功能是否有问题
# 更好的利用资源，节约人力资源
# 覆盖全面，增加软件稳定性
#
# 场景：
# 如果需求变更多，项目周期长，稳定性更强，环境要隔离独立（有单独的测试环境）
# 如果整个系统需求多，只能抽取部分功能做自动化
